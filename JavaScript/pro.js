function OpenPopup() {
    // JS for alert message to appear 
    alert("Your item has been added to cart!!!")
    // for the number of items in add to increase every time Add to cart button is clicked
    document.getElementById("ATC").style.visibility = "visible";
    i++;
    document.getElementById("ATC").innerHTML = "<h3>" + i + "</h3>"
}

